#Profundizar en los sistemas de numeracion

#Sistema decimal
a = 10
print(f'a: {a}')
#Sistema binario
a = 0b1010
print(f'a: {a}')
#Sistema octal
a = 0o12
print(f'a: {a}')

#Hexadecimal
a = 0xA
print(f'a: {a}')

#Base Decimal
a = int('23', 10)
print(f'a decimal: {a}')

#Base Binario
a = int('10111', 2)
print(f'a binario: {a}')

#Base Octal
a = int('27', 8)
print(f'a = base octal: {a}')

# Base hexadecimal
a = int('17', 16)
print(f'a = base hexadecimal: {a}')

#Base 5 sus valores son de 0 a 4
a = int('34', 5)
print(f'a = Base 5: {a}')